#!/usr/bin/env python3
"""
Quick Test - בדיקה מהירה שהסיכום עובד
======================================
הסקריפט הזה מדלג על שלב התמלול ובודק רק את הסיכום.
מושלם לבדיקה ראשונה!

שימוש:
    python3 quick_test.py
"""

import subprocess
import sys
import time

OLLAMA_MODEL = "qwen3:1.7b"

# טקסט לדוגמה - סימולציה של תמלול פגישה בעברית
SAMPLE_TRANSCRIPT = """
שלום לכולם, תודה שהגעתם לפגישה. אז היום אנחנו צריכים לדבר על שלושה נושאים עיקריים.
הנושא הראשון הוא ההשקה של המוצר החדש. אנחנו מתכננים להשיק בעוד שלושה שבועות, ב-25 למאי.
יוסי, אתה אחראי על הצד הטכני - איפה אנחנו עומדים?

יוסי: אנחנו בשלבים אחרונים. הבאגים הקריטיים תוקנו, נשארו עוד שני באגים קטנים שנסגור עד סוף השבוע.
אני צריך מדנה שתסיים את העיצוב של דף הנחיתה עד יום שלישי.

דנה: בסדר, אני יכולה לסיים את זה. אני רק צריכה את הטקסטים הסופיים ממיכל.

מיכל: אני אשלח את הטקסטים מחר בבוקר.

מצוין. הנושא השני הוא התקציב. אנחנו חורגים ב-15 אחוז מהתקציב המקורי.
ההחלטה שלי היא שנקצץ את תקציב הפרסום בפייסבוק ונעביר את הכסף לגוגל, כי שם אנחנו רואים תשואה טובה יותר.
אורי, אתה מטפל בזה?

אורי: כן, אני אעדכן את הקמפיינים עד יום חמישי.

הנושא השלישי - גיוס. אנחנו צריכים עוד מפתח פולסטאק. 
מיכל, את מתאמת את הראיונות. יש לנו שלושה מועמדים לשבוע הבא.

מיכל: נכון, הראיונות ביום שני ושלישי. אני אשלח לכולם את קורות החיים היום.

אוקיי, אז לסיכום: השקה ב-25 למאי, דנה מסיימת עיצוב עד שלישי, מיכל שולחת טקסטים מחר,
אורי מעדכן קמפיינים עד חמישי, וראיונות שבוע הבא. תודה לכולם!
"""


def test_summarization():
    """בדיקה מהירה של יכולת הסיכום"""
    print()
    print("╔══════════════════════════════════════════════╗")
    print("║   LocalScribe - בדיקה מהירה                 ║")
    print("╚══════════════════════════════════════════════╝")
    print()
    
    # בדיקה ש-Ollama רץ
    print("🔍 בודק ש-Ollama פעיל...")
    result = subprocess.run(["ollama", "list"], capture_output=True, text=True)
    if result.returncode != 0:
        print("❌ Ollama לא רץ! הפעל אותו עם: ollama serve")
        sys.exit(1)
    
    if OLLAMA_MODEL.split(":")[0] not in result.stdout:
        print(f"⬇️  מוריד מודל {OLLAMA_MODEL}...")
        subprocess.run(["ollama", "pull", OLLAMA_MODEL], check=True)
    
    print(f"✅ מודל {OLLAMA_MODEL} מוכן")
    print()
    
    # הצגת הטקסט לדוגמה
    print("─" * 50)
    print("📄 תמלול לדוגמה (סימולציה של פגישת צוות):")
    print("─" * 50)
    print(SAMPLE_TRANSCRIPT[:200] + "...")
    print("─" * 50)
    print()
    
    # סיכום
    print(f"🤖 מסכם עם {OLLAMA_MODEL}...")
    print("   (הפעם הראשונה יכולה לקחת 30-60 שניות)")
    print()
    
    prompt = f"""/no_think
אתה עוזר שמסכם פגישות בעברית. קיבלת תמלול של פגישה.
ספק:
1. **כותרת** - שם קצר לפגישה
2. **סיכום** - 3-5 משפטים
3. **משימות לביצוע** - רשימה ממוספרת (מי, מה, עד מתי)
4. **החלטות שהתקבלו**

התמלול:
---
{SAMPLE_TRANSCRIPT}
---

סכם בעברית:"""

    start_time = time.time()
    
    result = subprocess.run(
        ["ollama", "run", OLLAMA_MODEL, prompt],
        capture_output=True,
        text=True,
        timeout=120
    )
    
    elapsed = time.time() - start_time
    summary = result.stdout.strip()
    
    print(f"✅ סיכום הושלם ב-{elapsed:.1f} שניות!")
    print()
    print("═" * 50)
    print("📋 סיכום הפגישה:")
    print("═" * 50)
    print()
    print(summary)
    print()
    print("═" * 50)
    print()
    print("🎉 הכל עובד! עכשיו אתה יכול:")
    print("   • להקליט פגישה אמיתית")
    print("   • לתמלל קובץ אודיו קיים")
    print("   • להריץ: python3 transcribe_and_summarize.py")
    print()


if __name__ == "__main__":
    test_summarization()
